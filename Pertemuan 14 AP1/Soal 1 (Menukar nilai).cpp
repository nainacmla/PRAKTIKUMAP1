#include <iostream>
using namespace std;

int main() {
    int X = 10, Y = 23;

    // Menampilkan nilai awal X dan Y
    cout << "Semula Nilai X = " << X << " dan Y = " << Y << endl;

    // Menukarkan nilai X dan Y
    X = 23;
    Y = 10;

    // Menampilkan nilai setelah menukarkan
    cout << "Setelah ditukas Nilai X = " << X << " dan Y = " << Y << endl;

    return 0;
}